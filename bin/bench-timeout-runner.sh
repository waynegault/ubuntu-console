#!/bin/bash
# bench-timeout-runner.sh — Subprocess runner with PID tracking and cleanup.
#
# Extracted from __bench_run_with_timeout() in scripts/11-llm-manager.sh
# (card #0967f11c). Previously inlined as a ~40-line heredoc passed to
# setsid bash -lc, which made it impossible to debug when the subprocess
# silently exited 1 — no log, no trace, no way to replay the failure.
#
# This standalone script sources an optional profile/init script (so that
# shell functions are available in the new session), then runs the target
# command as a foreground child with traps that clean up orphans.
#
# Usage:
#   bin/bench-timeout-runner.sh [options] <profile_path> <command...>
#
# Options:
#   -p <pidfile>   Write the child PID to this file for external monitoring.
#   -l <logfile>   Append a structured log line to this file on start and exit.
#
# Positional arguments:
#   <profile_path>  Bash init script to source (e.g. env.sh). Skipped when
#                   empty or not found. Pass "" or "none" to skip explicitly.
#   <command...>    The command and all its arguments.
#
# Standalone debug invocation (from repo root):
#   bin/bench-timeout-runner.sh -p /tmp/bench-child.pid -l /tmp/bench-runner.log \
#     env.sh ./llm-bench --model test
#
# To verify the script works without touching the real bench infrastructure:
#   bin/bench-timeout-runner.sh "" sleep 2 && echo "exit: $?"
#
# Exit codes:
#   0   — Command completed normally (child exit status propagated)
#   124 — Timeout (SIGTERM sent, grace period expired)  *
#   130 — Interrupted (SIGINT)
#   143 — Terminated (SIGTERM)
#
#   * Note: The 124 exit code is generated by the parent __bench_run_with_timeout
#     function, not by this script itself. This script passes through the child's
#     exit status on normal completion.
#
# SAFETY: Uses SIGTERM-first with a grace period to let the child's EXIT traps
# fire. SIGKILL is only used as a last resort, and ONLY on the child's own
# process — never on parent process groups. This prevents parent EXIT traps
# (lock file cleanup, model stop) from being bypassed.

# AI INSTRUCTION: Increment version on significant changes.
# shellcheck disable=SC2034  # VERSION is read by external tooling, not this script
VERSION="1.0"  # Extracted from __bench_run_with_timeout (card #0967f11c).
# shellcheck disable=SC2317  # trap handlers are called by trap, not directly
set -euo pipefail

# --- Parse options -----------------------------------------------------------
pidfile=""
logfile=""

while getopts ":p:l:" _opt; do
    case "$_opt" in
        p) pidfile="$OPTARG" ;;
        l) logfile="$OPTARG" ;;
        *) echo "bench-timeout-runner: unknown option -$OPTARG" >&2
           exit 2 ;;
    esac
done
shift $((OPTIND - 1))

# --- Positional args ---------------------------------------------------------
profile_path="${1:-}"
shift 2>/dev/null || true

# --- Log helper --------------------------------------------------------------
__btr_log() {
    local _level="$1"
    shift
    if [[ -n "$logfile" ]]; then
        printf "[%s] [%s] %s\n" "$(date -u +%FT%TZ)" "$_level" "$*" >> "$logfile"
    fi
}

# --- Cleanup trap ------------------------------------------------------------
child_pid=""
# shellcheck disable=SC2317  # trap handlers are called by trap, not directly
__btr_cleanup() {
    local _exit_code=$?
    set +e
    if [[ -n "$child_pid" ]] && kill -0 "$child_pid" 2>/dev/null; then
        __btr_log "INFO" "cleaning up child pid $child_pid (exit $_exit_code)"
        kill -TERM -- "$child_pid" 2>/dev/null || true
        sleep 1
        kill -KILL -- "$child_pid" 2>/dev/null || true
    fi
    # Kill any remaining orphans under our session
    while IFS= read -r _cpid; do
        [[ "$_cpid" =~ ^[0-9]+$ ]] || continue
        kill -TERM -- "$_cpid" 2>/dev/null || true
    done < <(ps -o pid= --ppid $$ 2>/dev/null)
    __btr_log "EXIT" "cleanup complete (exit $_exit_code)"
    exit "$_exit_code"
}
trap __btr_cleanup EXIT INT TERM

# --- Source profile if provided -----------------------------------------------
if [[ -n "$profile_path" && "$profile_path" != "none" && -f "$profile_path" ]]; then
    __btr_log "INFO" "sourcing profile: $profile_path"
    # shellcheck disable=SC1090
    source "$profile_path" 2>/dev/null || true
fi

# --- Run the command ----------------------------------------------------------
__btr_log "INFO" "starting: $*"
"$@" &
child_pid=$!
__btr_log "INFO" "child pid: $child_pid"

# Write PID file
if [[ -n "$pidfile" ]]; then
    printf '%s\n' "$child_pid" > "$pidfile"
    __btr_log "INFO" "pidfile written: $pidfile → $child_pid"
fi

# Guard against empty child_pid (process already exited) which would
# cause wait "" to hang indefinitely waiting for non-existent children.
if [[ -n "$child_pid" ]] && [[ "$child_pid" =~ ^[0-9]+$ ]]; then
    wait "$child_pid"
    _wait_exit=$?
else
    wait
    _wait_exit=$?
fi

__btr_log "INFO" "child exited with code $_wait_exit"
exit "$_wait_exit"

# end of file

# end of file marker
